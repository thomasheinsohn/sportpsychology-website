function NavItem({
  href,
  children,
}: {
  href: string
  children: React.ReactNode
}) {
  const pathname = usePathname()

  let isActive = false
  if (href === '/') {
    isActive = pathname === '/de' || pathname === '/en'
  } else {
    isActive = pathname === `/de${href}` || pathname === `/en${href}`
  }
  // console.log('Current Path:', pathname, 'Href:', href, 'isActive:', isActive)

  return (
    <li>
      <Link
        href={href}
        className={clsx(
          'relative block px-3 py-2 transition',
          isActive
            ? 'text-sky-500 dark:text-sky-400'
            : 'hover:text-sky-500 dark:hover:text-sky-400',
        )}
      >
        {children}
        {isActive && (
          <span className="absolute inset-x-1 -bottom-px h-px bg-gradient-to-r from-sky-500/0 via-sky-500/40 to-sky-500/0 dark:from-sky-400/0 dark:via-sky-400/40 dark:to-sky-400/0" />
        )}
      </Link>
    </li>
  )
}

